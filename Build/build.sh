BASE_MAJOR=0
BASE_MINOR=1
BASE_BUILD=0
BASE_REVISION=0

ASSEMBLY_FILE_PATH=$1
BUILD_MAJOR=$2
BUILD_MINOR=$3
BUILD_NUMBER=$4
REVISION_NUMBER=$5

VERSION_TEMPLATE="(\"%s.%s.%s.%s\")"
INFORMATIONAL_VERSION_TEMPLATE="(\"%s.%s.%s\")"

VERSION_TO_REPLACE=`printf "$VERSION_TEMPLATE" $BASE_MAJOR $BASE_MINOR $BASE_BUILD $BASE_REVISION`
REPLACING_VERSION=`printf "$VERSION_TEMPLATE" $BUILD_MAJOR $BUILD_MINOR $BUILD_NUMBER $REVISION_NUMBER`

INFORMATIONAL_VERSION_TO_REPLACE=`printf "$INFORMATIONAL_VERSION_TEMPLATE" $BASE_MAJOR $BASE_MINOR $BASE_BUILD`
REPLACING_INFORMATIONAL_VERSION=`printf "$INFORMATIONAL_VERSION_TEMPLATE" $BUILD_MAJOR $BUILD_MINOR $BUILD_NUMBER`

sed -i "s/$VERSION_TO_REPLACE/$REPLACING_VERSION/g" "$ASSEMBLY_FILE_PATH"
sed -i "s/$INFORMATIONAL_VERSION_TO_REPLACE/$REPLACING_INFORMATIONAL_VERSION/g" "$ASSEMBLY_FILE_PATH"

